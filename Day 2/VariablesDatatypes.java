class VariablesDatatypes {
    public static void main(String[] args){
        String Name ="Sudharsan";
        int Myage = 22;
        double MyWeight = 60.0;
        boolean IsAlive = true;
        char grade = 'A';
        short trainNumber = 110;
        byte departmentNumber = 2;
        float trainWeight = 5.1F;

        System.out.println("Name: " + Name);
        System.out.println("Myage: "+ Myage);
        System.out.println("Myweight: "+ MyWeight);
        System.out.println("IsAlive: "+ IsAlive);
        System.out.println("Grade: "+ grade);
        System.out.println("Train number: "+ trainNumber);
        System.out.println("Department number: "+ departmentNumber);
        System.out.println("trainWeight: "+ trainWeight);

    }
}
